$(".level-super-easy").click(function () {
  level = levels[0];
  $(this).addClass("active2");
  cards = 6;

  for (let i = 0; i < cards; i++) {
    let newDiv = $(`<div data-number=${i+1} class = 'div div${i + 1}'>${i + 1}</div>`);
    $("body").append(newDiv);
  }

  $(".div").css("zoom", 1.3);

  // $('.div').css('zoom', '1')

  for (let i = 0; i < cards; i++) {
    let r = Math.floor((Math.random() * cards) / 2);
    let randomEmoji = emojiEasy[r];
    while (emojiList.filter((e) => e == randomEmoji).length == 2) {
      r = Math.floor((Math.random() * cards) / 2);
      randomEmoji = emojiEasy[r];
    }
    emojiList.push(randomEmoji);
  }

  console.log(emojiList);

  $(".div").each((i, el) => {
    $(el).click(function () {

      let textEl = $(el).text()
      if ($(".win").length >= cards) {
        return;
      }
      console.log(oldEmoji, "oldEmoji");

      if ($(el).hasClass("active") || $(el).hasClass("win")) {
        return;
      }

      if ($(".active").length == 2) {
        return;
      }

      $(el).addClass("active");

      if ($(".active").length == 1) {
        $(el).css("transform", "rotateY(90deg)");

        setTimeout(() => {
          $(el).text(emojiList[i]);
          $(el).css("transform", "rotateY(180deg)");
        }, 1000);
        oldEmoji = emojiList[i];

        return;
      }

      if (oldEmoji == emojiList[i]) {
        //  alert(1)
        $(el).addClass("win");
        setTimeout(() => {
          $(".div").removeClass("active");
        }, 2500);
        $(".div:contains(" + oldEmoji + ")").addClass("win");
        $(".active").addClass("win");
        openEmoji.push(emojiList[i]);

        console.log(".length", $(".win").length);
        console.log("cards", cards);

        if ($(".win").length >= cards) {
          localStorage.setItem("super-easy", true);
          setTimeout(function () {
          $(".you-win").text(text1)                                      //762495
            $(".you-win").css("display", "flex");
            jsConfetti.addConfetti();
          }, 2500);
        }
      }

      if (oldEmoji != emojiList[i]) {
        $(el).addClass("active");

        // alert(5)
        setTimeout(() => {
          $(".active").not(".win").css("transform", "rotateY(90deg)");
          setTimeout(() => {
            $(".active").not(".win").each((_, el)=>{
              $(el).text( $(el).data('number'));
            })
            $(".active").not(".win").css("transform", "rotateY(0deg)");
            $(".active").not(".win").removeClass("active");
          }, 1000);
        }, 1500);
      }

      $(el).css("transform", "rotateY(90deg)");

      setTimeout(() => {
        $(el).text(emojiList[i]);
        $(el).css("transform", "rotateY(180deg)");
      }, 1000);
    });
  });
});

